#include"printf.h"
#include<unistd.h>

int	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	while (*s)
	{
		write (fd, s++, 1);
		i++;
	}
	return (i);
}
